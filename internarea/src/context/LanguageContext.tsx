import React, { createContext, useContext, useState, useEffect, useCallback, ReactNode } from 'react';
import { LanguageCode } from '../types/types_l';

interface LanguageContextType {
  language: LanguageCode;
  setLanguage: (lang: LanguageCode) => void;
  translationLanguage: LanguageCode | null;
  setTranslationLanguage: (lang: LanguageCode | null) => void;
  isFrenchVerified: boolean;
  setFrenchVerified: (verified: boolean) => void;
  t: (key: string) => string;
}

const LanguageContext = createContext<LanguageContextType>({
  language: 'en',
  setLanguage: () => {},
  translationLanguage: null,
  setTranslationLanguage: () => {},
  isFrenchVerified: false,
  setFrenchVerified: () => {},
  t: (key: string) => key,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>('en');
  const [translationLanguage, setTranslationLanguageState] = useState<LanguageCode | null>(null);
  const [isFrenchVerified, setFrenchVerified] = useState(false);
  const [isMounted, setIsMounted] = useState(false);

  // Load saved language on mount (client-side only)
  useEffect(() => {
    setIsMounted(true);
    const savedLang = localStorage.getItem('lingosafe_lang');
    if (savedLang && ['en', 'es', 'hi', 'pt', 'zh', 'fr'].includes(savedLang)) {
      setLanguageState(savedLang as LanguageCode);
    }
    if (localStorage.getItem('lingosafe_french_verified') === 'true') {
      setFrenchVerified(true);
    }
  }, []);

  const setLanguage = useCallback((lang: LanguageCode) => {
    setLanguageState(lang);
    if (typeof window !== 'undefined') {
      localStorage.setItem('lingosafe_lang', lang);
    }
  }, []);

  const handleSetFrenchVerified = useCallback((verified: boolean) => {
    setFrenchVerified(verified);
    if (typeof window !== 'undefined') {
      localStorage.setItem('lingosafe_french_verified', String(verified));
    }
  }, []);

  const setTranslationLanguage = useCallback((lang: LanguageCode | null) => {
    setTranslationLanguageState(lang);
  }, []);

  // Translation function with fallback to English
  const t = useCallback((key: string): string => {
    if (!isMounted) {
      // During SSR, return English
      return getNestedTranslation(translations['en'], key) ?? key;
    }
    const activeLanguage = translationLanguage ?? language;
    const langTranslations = translations[activeLanguage] || translations['en'];
    const result = getNestedTranslation(langTranslations, key);
    if (result === undefined || result === null) {
      // Fallback to English
      const fallback = getNestedTranslation(translations['en'], key);
      return fallback ?? key;
    }
    return result;
  }, [language, translationLanguage, isMounted]);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, translationLanguage, setTranslationLanguage, isFrenchVerified, setFrenchVerified: handleSetFrenchVerified, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  return useContext(LanguageContext);
}

function getNestedTranslation(obj: any, path: string): string | undefined {
  const keys = path.split('.');
  let current = obj;
  for (const key of keys) {
    if (current === undefined || current === null) return undefined;
    current = current[key];
  }
  return typeof current === 'string' ? current : undefined;
}

